            <p>Upon successful completion of CS 210, students will be able
                to:</p>
            <ul>
                <li>understand, develop, and implement algorithms for
                    computational problem solving;
                <li> design, develop, and test computer programs in a
                    disciplined, systematic manner;
                </li>
                <li> demonstrate robust mental models of data representation and
                    code execution;
                </li>
                <li> demonstrate good understanding of a high level programming
                    language;
                </li>
                <li> implement a sampling of classic computer science problem
                    domains and algorithms
                </li>
